     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store()
    {


        $query = DB::table('sport_supervisor')->select('sport_id')->where('user_id', '=', Auth::user()->employeeid)->get();
        foreach ($query as $q){
            $sportid=$q->sport_id;
        }
        $blog=new Blog();
        $blog->sportname=$sportid;
        $blog->title=request('title');
        $blog->body=request('body');
        $blog->user_id=Auth::user()->employeeid;
        $blog->name=Auth::user()->name.' '.Auth::user()->surname;
        $blog->save();
        return redirect()->route('viewblog')->with('message','Blog successfully added');


    }

